const baninfo = () => {
  const list = document.querySelector('.banList');
  let newList = [];
  for(let i = 0; i < banList.effectMonsters.length; i++){
    let currentCard = banList.effectMonsters[i];
    newList += `<tr class = \"effectList\"><td>Effect Monster</td><td>${currentCard.name}</td><td>${currentCard.note}</td></tr>`;
  }
  for(let i = 0; i < banList.fusionMonsters.length; i++){
    let currentCard = banList.fusionMonsters[i];
    newList += `<tr class = \"fusionList\"><td>Fusion Monster</td><td>${currentCard.name}</td><td>${currentCard.note}</td></tr>`;
  }
  for(let i = 0; i < banList.linkMonsters.length; i++){
    let currentCard = banList.linkMonsters[i];
    newList += `<tr class = \"linkList\"><td>Link Monster</td><td>${currentCard.name}</td><td>${currentCard.note}</td></tr>`;
  }
  for(let i = 0; i < banList.synchroMonsters.length; i++){
    let currentCard = banList.synchroMonsters[i];
    newList += `<tr class = \"synchroList\"><td>Synchro Monster</td><td>${currentCard.name}</td> <td>${currentCard.note}</td></tr>`;
  }
  for(let i = 0; i < banList.xyzMonsters.length; i++){
    let currentCard = banList.xyzMonsters[i];
    newList += `<tr class = \"xyzList\"><td>XYZ Monster</td><td>${currentCard.name}</td> <td>${currentCard.note}</td></tr>`;
  }
  for(let i = 0; i < banList.spells.length; i++){
    let currentCard = banList.spells[i];
    newList += `<tr class = \"spellList\"><td>Spell</td><td>${currentCard.name}</td> <td>${currentCard.note}</td></tr>`;
  }
  for(let i = 0; i < banList.traps.length; i++){
    let currentCard = banList.traps[i];
    newList += `<tr class = \"trapList\"><td>Trap</td><td>${currentCard.name}</td> <td>${currentCard.note}</td></tr>`;
  }
  list.innerHTML += newList;
}

baninfo();
